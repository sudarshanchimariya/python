tuple3 = (2, 4, 6, 8, 10, 12, 14, 16)
t_slice = tuple3[1:2]
print('Slicing1: ', t_slice)

t_slice = tuple3[0:]
print('Slicing2: ', t_slice)

t_slice = tuple3[2:-2]
print('Slicing3: ', t_slice)

t_slice = tuple3[:3]
print('Slicing4: ', t_slice)

t_slice = tuple3[4:]
print('Slicing5: ', t_slice)

t_slice = tuple3[-3:-2]
print('Slicing6: ', t_slice)